function toggleMenu() {
    document.getElementById("mobile-menu").classList.toggle('hidden');
    document.getElementById("open-mobile-menu-icon").classList.toggle('hidden');
    document.getElementById("open-mobile-menu-icon").classList.toggle('block');
    document.getElementById("close-mobile-menu-icon").classList.toggle('hidden');
    document.getElementById("close-mobile-menu-icon").classList.toggle('block');
}